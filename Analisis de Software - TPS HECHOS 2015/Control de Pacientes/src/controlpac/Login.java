    private void ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarActionPerformed
        // TODO add your handling code here:
        getUsuario(); //Obtenemos el usuario.
        getPass(); //Obtenemos la contraseña.
        // Se muestra un mensaje si alguno de los campos está vacío.
        if(this.usuario=="" || this.contraseña==""){
            JOptionPane.showMessageDialog(null, "Por favor, ingrese nombre de usuario y contraseña."); //Mensaje de Error.
        }else{        
            if(!this.usuario.equals(this.usuarioValido)){
                // Si el usuario y la contraseña no son válidos.
               if(!this.contraseña.equals(this.contraseñaValida))
                   JOptionPane.showMessageDialog(null,"Usuario y Contraseña no son válidos. Vuelva a ingresar.");
               else // Sólo el usuario no es válido.
                   JOptionPane.showMessageDialog(null, "El usuario no es correcto. Vuelva a ingresar.");
            }else{ // Sólo la contraseña no es válida.
                if(!this.contraseña.equals(this.contraseñaValida))
                   JOptionPane.showMessageDialog(null,"Contraseña Incorrecta. Vuelva a ingresar.");
                else{ //Contraseña y usuario son válidas.
                    MainW menu=new MainW();
                    menu.setVisible(true); // hace visibla la ventana de menu principal.
                    setTitle("Menú Principal"); // Le da un titulo a la ventana.
                    menu.setLocationRelativeTo(null);
                    dispose();
                }
            }                   
        }            
    }//GEN-LAST:event_ingresarActionPerformed

    private void passwordKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passwordKeyTyped
        // TODO add your handling code here:
        // Creamos un evento para que cuando se presione la tecla Enter sea como presionar INGRESAR.
        //El usuario puede ingresar la contraseña e ingresar con un Enter.
        //******************************************************************************************
        char cTeclaPresionada=evt.getKeyChar();
        // da click en el botón Ingresar al detectar la tecla ENTER.
        if(cTeclaPresionada==KeyEvent.VK_ENTER){
            //Ejecuta el botón Ingresar (dar click)
            ingresar.doClick();
        }// fin del if.
              
    }//GEN-LAST:event_passwordKeyTyped

