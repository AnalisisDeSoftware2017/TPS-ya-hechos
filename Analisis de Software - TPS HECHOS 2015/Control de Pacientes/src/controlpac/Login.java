    public String getPass(){
       this.contraseña=""; //Cadena de String vacía para luego guardar la contraseña ingresada.
       char [] pass=password.getPassword(); // Obtenemos la contraseña que ingresó el usuario.
       for(int i=0; i<pass.length;i++){ //Debemos realizar un for para copiar la pass ingresada a la cadena.
           this.contraseña+=pass[i]; //Se copia.
       }
        return contraseña;
    }
    
  

